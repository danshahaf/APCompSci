

public class Student
{
    private String name;
    private double GPA;
    public Student(String name, double GPA)
    {
        name = this.name;
        GPA = this.GPA;
    }
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        name = this.name;
    }
    public double getGPA()
    {
        return GPA;
    }
    public void setGPA(double GPA)
    {
        GPA = this.GPA;
    }
}
