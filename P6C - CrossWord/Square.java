 

public class Square
   {
   private boolean isBlack;
   private int num;
   
   public Square( boolean initialIsBlack, int initialNum )
      {
      isBlack = initialIsBlack;
      num = initialNum;     
      } // end two-arg constructor
      
   public boolean getIsBlack()
      {
      return isBlack;     
      } // end method getIsBlack
      
   public int getNum()
      {
      return num;    
      } // end method getNum
       
      
   } // end class Square