public class WhileLoop
{
    public static void main()
    {
        //a for loop is good when you know the exact number of iterations
        //while loop can do the same or can be useful when there is unkown number of iterations
        int counter=0;
        while(counter <12)
        {
            System.out.println("Infinity" + counter);
            
            counter ++;
        }
    }
}